<script setup>
import { onMounted, ref } from 'vue';

const model = defineModel({
    required: true,
});

const input = ref(null);

onMounted(() => {
    if (input.value.hasAttribute('autofocus')) {
        input.value.focus();
    }
});

defineExpose({ focus: () => input.value.focus() });
</script>

<template>
    <input type="date" class="border-gray-300 focus:border-accent focus:ring-accent rounded-md shadow-sm"
        v-model="model" ref="input" />
</template>
